	$pm = new plugin_manager(); 
	$plugin_language = $pm->plugin_language("socialmedia", $plugin_path);

	$plugin_data= array();
    $plugin_data['$title']=$plugin_language['discord'];
    $plugin_data['$subtitle']='Discord';
    $template = $GLOBALS["_template"]->loadTemplate("discord","head", $plugin_data, $plugin_path);
    echo $template;

$sql = safe_query("SELECT * FROM ".PREFIX."settings_social_media");
if(mysqli_num_rows($sql)) {
    while($row = mysqli_fetch_array($sql)) {
    	$discord_login = $row['discord'];

        if(isset($_COOKIE['im_discord'])) {
            $discord = '<div data-service="discord" data-id="'.$discord_login.'" data-title="Discord" data-autoscale data-widget style="height: 450px"> </div>';
        } else {
            $discord = '<div data-service="discord" data-id="'.$discord_login.'" data-title="Discord" data-widget style="min-height: 250px;width: 300px"></div>';
        }   	

		$data_array = array();
		$data_array['$discord'] = $discord;
			
        $discord = $GLOBALS["_template"]->loadTemplate("discord","widget_content", $data_array, $plugin_path);
        echo $discord;
    }
}
