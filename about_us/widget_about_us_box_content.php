// Sprachdateien aus dem Plugin-Ordner laden
    $pm = new plugin_manager(); 
    $plugin_language = $pm->plugin_language("about_us", $plugin_path);

// Holen der Daten aus der Datenbank
$ergebnis = safe_query("SELECT * FROM " . PREFIX . "plugins_about_us");
if (mysqli_num_rows($ergebnis)) {
    $ds = mysqli_fetch_array($ergebnis);

    // Titel und Text aus der DB
    $title = $ds['title'];
    $text = $ds['text'];

    // Übersetzungen durchführen
    $translate = new multiLanguage(detectCurrentLanguage());
    $translate->detectLanguages($title);
    $title = $translate->getTextByLanguage($title);

    $translate->detectLanguages($text);
    $text = $translate->getTextByLanguage($text);

    // Maximale Zeichenanzahl aus den Einstellungen holen
    $maxaboutchars = !empty($ds['aboutchars']) ? $ds['aboutchars'] : 200;  // Default auf 200, wenn leer

    // HTML-Tags entfernen
    $text = preg_replace("/<(div|p|strong|em|s|u|blockquote|img|span)[^>]*>/", "", $text);

    // Text auf die maximal erlaubte Zeichenanzahl kürzen und "Weiterlesen"-Link hinzufügen
    $text = substr($text, 0, $maxaboutchars) . ' ...';

    // Template-Daten vorbereiten
    $data_array = array();
    $data_array['$title'] = $title;
    $data_array['$text'] = $text;

    // Template laden und anzeigen
    $about = $GLOBALS["_template"]->loadTemplate("about_us_box_content", "widget_content_two", $data_array, $plugin_path);
    echo $about;

} else {
    // Keine About-Daten gefunden, Standardanzeige
    $plugin_data= array();
    $plugin_data['$title']=$plugin_language['title'];
    
    $template = $GLOBALS["_template"]->loadTemplate("about_us", "widget_head", $plugin_data, $plugin_path);
    echo $template;
    echo $plugin_language['no_about'];
}